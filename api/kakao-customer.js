module.exports = async function handler(req, res) {
  if (req.method !== 'POST') {
    res.status(405).json({ error: 'Method Not Allowed' });
    return;
  }

  const utterance = req.body?.userRequest?.utterance || '';

  // 제휴 관련 키워드 감지 → 파트너 페이지 안내
  if (utterance && PARTNER_KEYWORDS.some((kw) => utterance.includes(kw))) {
    res.status(200).json(PARTNER_CARD);
    return;
  }

  if (!utterance) {
    res.status(200).json({
      version: '2.0',
      template: {
        outputs: [{ simpleText: { text: '무엇을 도와드릴까요?' } }],
      },
    });
    return;
  }

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01',
      },
      body: JSON.stringify({
        model: 'claude-haiku-4-5-20251001',
        max_tokens: 500,
        system: SYSTEM_PROMPT,
        messages: [{ role: 'user', content: utterance }],
      }),
    });

    const data = await response.json();
    const text = data?.content?.[0]?.text || '잠시 후 다시 시도해 주세요.';

    const outputs = [{ simpleText: { text } }];

    // 홈페이지 안내가 포함된 경우에만 버튼 카드 추가
    if (text.includes('홈페이지')) {
      outputs.push({
        basicCard: {
          thumbnail: {
            imageUrl: 'https://www.wishfulstay.com/images/kakao-thumbnail.png',
          },
          buttons: [
            {
              action: 'webLink',
              label: '홈페이지 바로가기',
              webLinkUrl: 'https://www.wishfulstay.com',
            },
          ],
        },
      });
    }

    res.status(200).json({
      version: '2.0',
      template: { outputs },
    });
  } catch (err) {
    console.error('[kakao-customer] 오류:', err);
    res.status(200).json({
      version: '2.0',
      template: {
        outputs: [{ simpleText: { text: '일시적인 오류가 발생했습니다. 잠시 후 다시 시도해 주세요.' } }],
      },
    });
  }
};
